#include <stdio.h>

int main(void)
{
    int nX = 1, nY = 2;
    printf("\nInit values: X= %d, Y= %d\n", nX, nY);
    nX = 1 + 2, 2 * 3, 3 + 4; /* в изразите може да има фции*/ // Изразът ще се изпълни от дясно наляво
    nY = (7 * 8, 8 + 9, 9 - 4); // Поради наличието на скоби, операторът запетая се изпълнява от ляво на дясно
    printf("\nX= %d, Y= %d\n", nX, nY);
    
    return 0;
}
